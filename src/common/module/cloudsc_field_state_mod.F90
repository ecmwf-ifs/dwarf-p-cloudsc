    INTEGER(KIND=JPIM) :: B

    ALLOCATE(FIELD_PTR)
    FIELD_PTR = FIELD_2D(SHAPE=SHAPE, NBLOCKS=NBLOCKS, PERSISTENT=.TRUE.)
    ! Due to Fortran's reallocation-on-assignment behaviour, the
    ! FIELD_PTR%PTR pointer needs updating here for owned objects.
