    IF (PRESENT(ZERO)) THEN
      IF (ZERO) THEN
        !$OMP PARALLEL DO DEFAULT(SHARED), PRIVATE(B) SCHEDULE(RUNTIME)
        DO B=1, NBLOCKS
          FIELD_PTR%PTR(:,B) = 0.0_JPRB
        END DO
        !$OMP END PARALLEL DO
      END IF
    END IF
