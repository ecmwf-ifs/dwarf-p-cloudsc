  RTWAT_RTICE_R_D=RTWAT_RTICE_R; RTWAT_RTICECU_R_D=RTWAT_RTICECU_R; RKOOP1_D=RKOOP1; RKOOP2_D=RKOOP2
END SUBROUTINE YOETHF_UPDATE_DEVICE

END MODULE YOETHF_CUF
